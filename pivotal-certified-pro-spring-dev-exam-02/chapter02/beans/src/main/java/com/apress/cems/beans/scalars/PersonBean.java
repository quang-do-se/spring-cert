package com.apress.cems.beans.scalars;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;

import java.time.LocalDateTime;

/**
 * @author Iuliana Cosmina
 * @since 1.0
 */
@Component
public class PersonBean implements Creature {

    private LocalDateTime birthDate;
    private String name;

    private Logger logger = LoggerFactory.getLogger(PersonBean.class);

    @Autowired
    public PersonBean(@Value("1977-10-16 00:00") LocalDateTime birthDate, @Value("John Mayer") String name) {
        logger.info("Person constructor is called...");
        this.birthDate = birthDate;
        this.name = name;
    }

    @Override
    public LocalDateTime getBirthDate() {
        return birthDate;
    }

    @Override
    @Autowired
    public void setBirthDate(@Value("1977-10-16 00:00") LocalDateTime birthDate) {
        logger.info("Person setter is called...");
        this.birthDate = birthDate;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "PersonBean{" +
                "birthDate=" + birthDate +
                ", name='" + name + '\'' +
                '}';
    }
}
